    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
    String namaPembeli = jTextField1.getText();
    String uangStr = jTextField4.getText();

    if (namaPembeli.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama pembeli wajib diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (uangStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah uang bayar!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (modelKeranjang.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Keranjang masih kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int total = Integer.parseInt(jTextField5.getText());
    int bayar = Integer.parseInt(jTextField4.getText());

    if (bayar < total) {
        JOptionPane.showMessageDialog(this, "Uang bayar kurang dari total!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int kembali = bayar - total;

    DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
    String waktu = dtf.format(LocalDateTime.now());

    try {
        PreparedStatement ps = conn.prepareStatement("INSERT INTO transaksi (nama_pembeli, total, bayar, kembali, waktu) VALUES (?, ?, ?, ?, ?)");
        ps.setString(1, namaPembeli);
        ps.setInt(2, total);
        ps.setInt(3, bayar);
        ps.setInt(4, kembali);
        ps.setString(5, waktu);
        ps.executeUpdate();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal simpan transaksi: " + e.getMessage());
    }

    StringBuilder struk = new StringBuilder();
    struk.append("==== TOKO PEMPEK ====\n");
    struk.append("Nama: ").append(namaPembeli).append("\n");
    struk.append("Waktu: ").append(waktu).append("\n");
    struk.append("-------------------------\n");

    for (int i = 0; i < modelKeranjang.getRowCount(); i++) {
        String nama = modelKeranjang.getValueAt(i, 0).toString();
        int harga = Integer.parseInt(modelKeranjang.getValueAt(i, 1).toString());
        int jumlah = Integer.parseInt(modelKeranjang.getValueAt(i, 2).toString());
        struk.append(nama).append(" x").append(jumlah).append(" = ").append(harga * jumlah).append("\n");
    }

    struk.append("-------------------------\n");
    struk.append("Total: ").append(total).append("\n");
    struk.append("Bayar: ").append(bayar).append("\n");
    struk.append("Kembali: ").append(kembali).append("\n");

    jTextArea1.setText(struk.toString());

    // Reset
    modelKeranjang.setRowCount(0);
    jTextField5.setText("");
    jTextField4.setText("");


    }//GEN-LAST:event_jButton4ActionPerformed


void hitungTotal() {
    int total = 0;
    for (int i = 0; i < modelKeranjang.getRowCount(); i++) {
        total += Integer.parseInt(modelKeranjang.getValueAt(i, 3).toString());
    }
    jTextField5.setText(String.valueOf(total));
}

void loadProduk() {
    modelProduk = new DefaultTableModel(new String[]{"ID", "Nama Produk", "Harga"}, 0);
    jTable1.setModel(modelProduk);

    try {
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM produk");
        while (rs.next()) {
            modelProduk.addRow(new Object[]{
                rs.getInt("id_produk"),
                rs.getString("nama_produk"),
                rs.getInt("harga")
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal load produk: " + e.getMessage());
    }
}


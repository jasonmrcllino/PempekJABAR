    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
  // Validasi input jumlah
String jumlahStr = jTextField3.getText();
if (jumlahStr.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Jumlah harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

int selected = jTable1.getSelectedRow();
if (selected == -1) {
    JOptionPane.showMessageDialog(this, "Pilih produk terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}

try {
    int jumlah = Integer.parseInt(jumlahStr);
    if (jumlah <= 0) {
        JOptionPane.showMessageDialog(this, "Jumlah harus lebih dari 0!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String nama = modelProduk.getValueAt(selected, 1).toString();
    int harga = Integer.parseInt(modelProduk.getValueAt(selected, 2).toString());
    int subtotal = harga * jumlah;

    modelKeranjang.addRow(new Object[]{nama, harga, jumlah, subtotal});
    hitungTotal();
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
}

    }//GEN-LAST:event_jButton1ActionPerformed

